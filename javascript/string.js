// charAt()
var txt = "Hello world";
console.log(txt.charAt(1)); // Output: "e"

// charCodeAt()
var txt = "Hello World!";
console.log(txt.charCodeAt(0)); // Output: 72

// concat()
var str1 = "Hello";
var str2 = "World";
console.log(str1.concat(" ", str2)); // Output: "Hello World"

// indexOf()
var txt = "Hello World!";
console.log(txt.indexOf("o")); // Output: 4

// lastIndexOf()
var txt = "Hello World!";
console.log(txt.lastIndexOf("o")); // Output: 7

// replace()
var txt = "Hello World!";
console.log(txt.replace("World", "Universe")); // Output: "Hello Universe!"

// slice()
var txt = "Hello World!";
console.log(txt.slice(0, 5)); // Output: "Hello"

// split()
var txt = "Hello World!";
console.log(txt.split(" ")); // Output: ["Hello", "World!"]

// substring()
var txt = "Hello World!";
console.log(txt.substring(0, 5)); // Output: "Hello"

// toLowerCase()
var txt = "Hello World!";
console.log(txt.toLowerCase()); // Output: "hello world!"

// toUpperCase()
var txt = "Hello World!";
console.log(txt.toUpperCase()); // Output: "HELLO WORLD!"
